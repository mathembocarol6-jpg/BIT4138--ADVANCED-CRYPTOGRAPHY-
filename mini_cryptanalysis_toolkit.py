def analyze_input_differences(txt1, txt2):
    b1 = [ord(c) for c in txt1]
    b2 = [ord(c) for c in txt2]
    max_len = max(len(b1), len(b2))
    b1 += [0] * (max_len - len(b1))
    b2 += [0] * (max_len - len(b2))
    
    xor_diffs = []
    bit_flips = 0
    for x, y in zip(b1, b2):
        diff = x ^ y
        xor_diffs.append(f"0x{diff:02X}")
        bit_flips += bin(diff).count('1')
        
    return xor_diffs, bit_flips, max_len * 8

def perform_frequency_analysis(hex_string):
    tokens = hex_string.upper().split()
    total = len(tokens)
    if total == 0:
        return {}
    counts = collections.Counter(tokens)
    return {tok: (count, (count / total) * 100) for tok, count in counts.items()}

def measure_statistical_bias(hex_string):
    tokens = hex_string.upper().split()
    total_bits = len(tokens) * 8
    if total_bits == 0:
        return 0, 0, 0
        
    ones = 0
    for tok in tokens:
        try:
            val = int(tok, 16)
            ones += bin(val).count('1')
        except ValueError:
            continue
            
    zeros = total_bits - ones
    bias = abs((ones / total_bits) - 0.5)
    return ones, zeros, bias

